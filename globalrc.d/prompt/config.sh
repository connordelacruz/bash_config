# ==============================================================================
# Configure PS1 Prompt
# ==============================================================================

# General
# ----------------

# Prompt symbol
PS_SYMBOL='\$'
# XTerm window title (for compatible terminals)
XTERM_WINDOW_TITLE="\[\e]0;\w\a\]"

# Powerline
# ----------------

# Set to 1 to use powerline.sh instead of a standard PS1
POWERLINE_ENABLE=1
# Set to 1 to enable git info
POWERLINE_GIT=1
# Set to 1 to enable python virtualenv info
POWERLINE_VIRTUALENV=1
# Set to 1 to enable info on the number of background jobs running
POWERLINE_JOBS=1
# Set to 1 to enable success/failure symbol color changes
POWERLINE_EXIT_CODE_COLOR=0
# Set to 1 to show user in powerline
POWERLINE_SHOW_USER=0

